/* --- Types -------------------------------------------------------------------- */
export interface ScheduleConfig {
  templateKey: string;
  title:       string;
  frequency:   "DAILY" | "WEEKLY" | "MONTHLY";
  recipients:  string[];
  createdBy:   string;
}

/* --- Helpers ------------------------------------------------------------------ */
function generateReportId(): string {
  return `RPT-${Date.now().toString(36).toUpperCase()}-${randomBytes(2).toString("hex").toUpperCase()}`;
}

function nextRunDate(frequency: "DAILY" | "WEEKLY" | "MONTHLY"): Date {
  const d = new Date();
  if (frequency === "DAILY")   d.setDate(d.getDate() + 1);
  if (frequency === "WEEKLY")  d.setDate(d.getDate() + 7);
  if (frequency === "MONTHLY") d.setMonth(d.getMonth() + 1);
  d.setHours(6, 0, 0, 0); // 6 AM BST
  return d;
}

